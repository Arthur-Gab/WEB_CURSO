/*
    Mouse Events
    onclick - clique 1x
    ondblclick - clique 2x
    onmouseup - clique liberado
    onmouseover - cursor em cima do elemento
    onmouseout - cursor sai de cima do elemento
*/

function alertBtn() {
    alert('Evento de alert');
}

function alertBtnClick() {
    alert('Clicou no button');
}

function alertBtnOut() {
    alert('Cursor saiu da área do button');
}

/*
    Teclado Events
    onkeydown - tecla pressionada
    onkeypress - mantem a tecla pressionada (teclas com caracter)
    onkeyup - tecla liberada
*/

function teclaAlert() {
    alert('Evento de teclado disparado');
}

/*
    Janela/Frame Events
    onresize - capturar a janela e disparar uma ação dependendo da resolução (body)
    onscroll - quando o scroll do mouse é acionado
*/

/*
    Formulários Events
    onfocus - recebe o clique
    onblur - perde o foco
    onchange - estado do elemtno modificado (type = 'select')
*/
